public class Primitivos {
    public static void main(String[] args) {
        byte numeroByte = 7;
        System.out.println("numeroByte =" + numeroByte );
        System.out.println("Tipo byte corresponde en byte " + Byte.BYTES);
        System.out.println("Tipo byte corresponde en bite " + Byte.SIZE);
        System.out.println("Valor maximo de un Byte: " + Byte.MAX_VALUE);
        System.out.println("Valor minio de un Byte: " + Byte.MIN_VALUE);

        short numeroShort = 30000;
        System.out.println("Numero short =" + numeroShort );
        System.out.println("Tipo short corresponde en byte " + Short.BYTES);
        System.out.println("Tipo short corresponde en bite " + Short.SIZE);
        System.out.println("Valor maximo de un short: " + Short.MAX_VALUE);
        System.out.println("Valor minio de un short: " + Short.MIN_VALUE);

    }
}
