/* en este ejercio se pretenten demostrar como se utilizan los operadores unarios que son
multiplicar por -1 o por +1 utilies en formilas matematicas

 */

package semana03;

public class OperadoresUnarios {
    public static void main(String[] args) {
        int i = -5;

        int j = +i; // j = (1)* j
        System.out.println("j = " + j);

        int k = -i; // k = (-1)* i
        System.out.println("k = " + k);

    }
}
